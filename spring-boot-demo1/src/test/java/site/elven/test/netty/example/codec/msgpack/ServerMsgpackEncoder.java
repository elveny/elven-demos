package site.elven.test.netty.example.codec.msgpack;

import io.netty.buffer.ByteBuf;
import io.netty.channel.ChannelHandlerContext;
import io.netty.handler.codec.MessageToByteEncoder;
import org.msgpack.MessagePack;

public class ServerMsgpackEncoder extends MessageToByteEncoder<OrderResponse> {
    @Override
    protected void encode(ChannelHandlerContext ctx, OrderResponse msg, ByteBuf out) throws Exception {
        MessagePack msgpack = new MessagePack();
        byte[] raw = msgpack.write(msg);
        out.writeBytes(raw);
    }
}
